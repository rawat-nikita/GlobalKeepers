Obtaining a quality education is the foundation to improving people's lives and sustainable development.
Major progress has been made towards increasing access to education at all levels and increasing enrolment
rates in schools particularly for women and girls. Basic literacy skills have improved tremendously, yet 
bolder efforts are needed to make even greater strides for achieving universal education goals. For example,
the world has achieved equality in primary education between girls and boys, but few countries have achieved
that target at all levels of education.
<br/>
<h1><font color="black">Goal Targets</font></h1>
1) By 2030, ensure that all girls and boys complete free, equitable and quality primary and secondary education 
    leading to relevant and Goal-4 effective learning outcomes.<br/>
2) By 2030, ensure that all girls and boys have access to quality early childhood development, care and preprimary 
   education so that they are ready for primary education.<br/>
3) By 2030, ensure equal access for all women and men to affordable and quality technical, vocational and tertiary 
    education, including university.<br/>
4) By 2030, substantially increase the number of youth and adults who have relevant skills, including technical and 
    vocational skills, for employment, decent jobs and entrepreneurship.<br/>
5) By 2030, eliminate gender disparities in education and ensure equal access to all levels of education and vocational 
     training for the vulnerable, including persons with disabilities, indigenous peoples and children in vulnerable 
     situations.<br/>
6) By 2030, ensure that all youth and a substantial proportion of adults, both men and women, achieve literacy and numeracy.<br/>
7) By 2030, ensure that all learners acquire the knowledge and skills needed to promote sustainable development, including,
    among others, through education for sustainable development and sustainable lifestyles, human rights, gender equality,
    promotion of a culture of peace and non-violence, global citizenship and appreciation of cultural diversity and of 
    culture's contribution to sustainable development.<br/>
8) Build and upgrade education facilities that are child, disability and gender sensitive and provide safe, nonviolent, 
    inclusive and effective learning environments for all.<br/>

