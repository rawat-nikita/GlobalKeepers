<font color="blue" size="5px">
Roughly half the world's population still lives on the equivalent of about US$2 a day. And in too many places,
having a job doesn't guarantee the ability to escape from poverty. This slow and uneven progress requires us to 
rethink and retool our economic and social policies aimed at eradicating poverty.
A continued lack of decent work opportunities, insufficient investments and under-consumption lead to an erosion 
of the basic social contract underlying democratic societies: that all must share in progress.The creation of quality
jobs will remain a major challenge for almost all economies well beyond 2015.

Sustainable economic growth will require societies to create the conditions that allow people to have quality jobs that
stimulate the economy while not harming the environment. Job opportunities and decent working conditions are also required 
for the whole working age population.
<br/>
<h1><font color="black">Goals Targets</font></h1>
1) Sustain per capita economic growth in accordance with national circumstances and, in particular, at least 7 per cent
    gross domestic product growth per annum in the least developed countries.<br/>
2) Achieve higher levels of economic productivity through diversification, technological upgrading and innovation, 
    including through a focus on high-value added and labour-intensive sectors.<br/>
3) Promote development-oriented policies that support productive activities, decent job creation, entrepreneurship, 
    creativity and innovation, and encourage the formalization and growth of micro-, small- and medium-sized 
    enterprises, including through access to financial services.<br/>
4) Improve progressively, through 2030, global resource efficiency in consumption and production and endeavour to
    decouple economic growth from environmental degradation, in accordance with the 10-year framework of programmes on 
    sustainable consumption and production, with developed countries taking the lead.<br/>
5) By 2030, achieve full and productive employment and decent work for all women and men, including for young people and
      persons with disabilities, and equal pay for work of equal value.<br/>
6) By 2020, substantially reduce the proportion of youth not in employment, education or training.<br/>
7) Take immediate and effective measures to eradicate forced labour, end modern slavery and human trafficking and secure
     the prohibition and elimination of the worst forms of child labour, including recruitment and use of child soldiers, 
     and by 2025 end child labour in all its forms.<br/>
8) Protect labour rights and promote safe and secure working environments for all workers, including migrant workers, in 
    particular women migrants, and those in precarious employment.
