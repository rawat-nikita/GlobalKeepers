 <h1><font color="black">2) Food security</font></h1>
Agriculture is the single largest employer in the world, providing livelihoods for 40 per cent of 
today's global population. It is the largest source of income and jobs for poor rural households.
500 million small farms worldwide, most still rainfed, provide up to 80 per cent of food consumed 
in a large part of the developing world. Investing in smallholder women and men is an important way 
to increase food security and nutrition for the poorest, as well as food production for local and global 
markets.Since the 1900s, some 75 per cent of crop diversity has been lost from farmers fields. Better use 
of agricultural biodiversity can contribute to more nutritious diets, enhanced livelihoods for farming communities
and more resilient and sustainable farming systems.If women farmers had the same access to resources as men, 
the number of hungry in the world could be reduced by up to 150 million.
1.4 billion people have no access to electricity worldwide-most of whom live in rural areas of the developing world. Energy poverty in many regions is a fundamental barrier to reducing hunger and ensuring that the world can produce enough food to meet future demand.</font>

