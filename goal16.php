<h1><font color="black">Goal Targets</font></h1>
1) Significantly reduce all forms of violence and related death rates everywhere.<br/>
2) End abuse, exploitation, trafficking and all forms of violence against and torture of children.<br/>
3) Promote the rule of law at the national and international levels and ensure equal access to justice for all.<br/>
4) By 2030, significantly reduce illicit financial and arms flows, strengthen the recovery and return of stolen 
    assets and combat all forms of organized crime.<br/>
5) Substantially reduce corruption and bribery in all their forms.<br/>
6) Develop effective, accountable and transparent institutions at all levels.<br/>
7) Ensure responsive, inclusive, participatory and representative decision-making at all levels.<br/>
8) Broaden and strengthen the participation of developing countries in the institutions of global governance.<br/>
9) By 2030, provide legal identity for all, including birth registration.<br/>
10) Ensure public access to information and protect fundamental freedoms, in accordance with national legislation 
     and international agreements.<br/>
11) Strengthen relevant national institutions, including through international cooperation, for building capacity at
      all levels, in particular in developing countries, to prevent violence and combat terrorism and crime.<br/>
12) Promote and enforce non-discriminatory laws and policies for sustainable development.
