<h1><font color="black">Goal Targets</font></h1>
<h1><font color="black">Finance</font></h1>
1) Strengthen domestic resource mobilization, including through international support to developing countries, to improve 
    domestic capacity for tax and other revenue.<br/>
2) Developed countries to implement fully their official development assistance commitments, including the commitment by 
    many developed countries to achieve the target of 0.7 per cent of ODA/GNI to developing countries and 0.15 to 0.20 per 
    cent of ODA/GNI to least developed countries ODA providers are encouraged to consider setting a target to provide at least
    0.20 per cent of ODA/GNI to least developed countries.<br/>
3) Mobilize additional financial resources for developing countries from multiple sources.<br/>
4) Assist developing countries in attaining long-term debt sustainability through coordinated policies aimed at fostering 
    debt financing, debt relief and debt restructuring, as appropriate, and address the external debt of highly indebted 
    poor countries to reduce debt distress.<br/>
5) Adopt and implement investment promotion regimes for least developed countries.

<h1><font color="black">Technology</font></h1>
1) Enhance North-South, South-South and triangular regional and international cooperation on and access to science, 
    technology and innovation and enhance knowledge sharing on mutually agreed terms, including through improved 
    coordination among existing mechanisms, in particular at the United Nations level, and through a global technology
    facilitation mechanism.<br/>
2) Promote the development, transfer, dissemination and diffusion of environmentally sound technologies to developing 
    countries on favourable terms, including on concessional and preferential terms, as mutually agreed.<br/>
3) Fully operationalize the technology bank and science, technology and innovation capacity-building mechanism for least 
    developed countries by 2017 and enhance the use of enabling technology, in particular information and communications 
    technology.

<h1><font color="black">Capacity Building</font></h1>
1) Enhance international support for implementing effective and targeted capacity-building in developing countries to 
    support national plans to implement all the sustainable development goals, including through North-South, South-
    South and triangular cooperation.
 
<h1><font color="black">Trade</font></h1>
1) Promote a universal, rules-based, open, non-discriminatory and equitable multilateral trading system under the World Trade
     Organization, including through the conclusion of negotiations under its Doha Development Agenda.<br/>
2) Significantly increase the exports of developing countries, in particular with a view to doubling the least developed countries
    share of global exports by 2020.<br/>
3) Realize timely implementation of duty-free and quota-free market access on a lasting basis for all least developed countries, 
    consistent with World Trade Organization decisions, including by ensuring that preferential rules of origin applicable to 
    imports from least developed countries are transparent and simple, and contribute to facilitating market access.
